def searchRange(A, B):
    starting = 0
    ending = 0

    if A[0] == B:
        starting = 0
    else:
        start = 1
        end = len(A) - 1
        while start <= end:
            mid = start + (end - start) / 2
            if A[mid] == B and A[mid-1] < B:
                starting = mid
                break
            elif A[mid] >= B:
                end = mid - 1
            else:
                start = mid + 1
    if A[-1] == B:
        ending = len(A) - 1
    else:
        start = 0
        end = len(A) - 2
        while start <= end:
            mid = start + (end - start) / 2
            if A[mid] == B and A[mid+1]> B:
                ending = mid + 1
                break
            elif A[mid] > B:
                end = mid - 1
            else:
                start = mid + 1
    starting = int(starting)
    ending = int(ending)-1
    if len(A) == 1:
        ending = 0
    if ending == -1:
        starting = -1
    return starting, ending

from bisect import bisect_right, bisect_left

def searchRangeII(A, B):
    left, right = bisect_left(A,B), bisect_right(A,B)
    if left == right:
        return [-1, 1]
    else:
        return [left, right-1]




